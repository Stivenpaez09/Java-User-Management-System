    private void cargarDatos(int id) {
        us = control.traerUsuario(id);
        txtUsuario.setText(us.getUsuario());
        txtContra.setText(us.getContrasenia());
        
        
        List<Rol> listaRoles = control.traerRoles();
        
        if(listaRoles!=null){
            for(Rol rol : listaRoles){
            cmbRol.addItem(rol.getNombreRol());
            }
        }
        
        String rol = us.getRol().getNombreRol();
        int cantidadItems = cmbRol.getItemCount();
        
        for(int i=0; i<cantidadItems; i++){
            if(String.valueOf(cmbRol.getItemAt(i)).equals(rol)){
                cmbRol.setSelectedIndex(i);
            }
        }
    }
    
    private void cerrarVentana(){
        pAdmin.setVisible(true);
        pAdmin.setLocationRelativeTo(null);
        this.dispose();
    }
