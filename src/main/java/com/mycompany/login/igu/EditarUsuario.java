    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String usuario = txtUsuario.getText();
        String contra = txtContra.getText();
        String rol = (String)cmbRol.getSelectedItem();
        
        boolean compUsuario = control.compararUsuarios(usr, usuario, rol);
        
        control.editarUsuario(us, usuario, contra, rol);
        JOptionPane.showMessageDialog(null, "Se edito el usuario exitosamente", "Usuario edtado", JOptionPane.INFORMATION_MESSAGE);
        if(!compUsuario){
            this.cerrarVentana();
        } else {            
            pInicial.setVisible(true);
            pInicial.setLocationRelativeTo(null);
            this.dispose();
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

